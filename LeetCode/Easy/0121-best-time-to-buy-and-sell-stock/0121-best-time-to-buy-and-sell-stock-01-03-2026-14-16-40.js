/**
 * @param {number[]} prices
 * @return {number}
 */
var maxProfit = function (prices) {
    let left = 0
    let maxProfit = 0

    for (let right = 1; right < prices.length; right++) {
        if (prices[right] < prices[left]) {
            left = right
        }
        if ((prices[right] - prices[left]) > maxProfit) {
            maxProfit = prices[right] - prices[left]
        }
    }
    return maxProfit

};